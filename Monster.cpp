/*

  The Monster class creates objects with the states:
  string name, int health, int armore, and int strength


*/
///////////////////////////////////////////////////////////////////////////////////////////////////////////////


#include "Monster.hpp"

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//--------------------------------------------- Class Monster ----------------------------------------------------
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//----------------------------------------------------------------------------------

// Constructors
//----------------------------------------------------------------------------------

// Default constructor
Monster::Monster()
{
	// empty
}

Monster::Monster(string name, int health, int armor, int strength) 
{

	this->name = name;
	this->health = health;
	this->armor = armor;
	this->strength = strength;
}

//----------------------------------------------------------------------------------
//----------------------------------------------------------------------------------

// Accessors 
//----------------------------------------------------------------------------------

string Monster::get_name() 
{
	return name;
}

int Monster::get_health() 
{
	return health;
}

int Monster::get_armor() 
{
	return armor;
}

int Monster::get_strength() 
{
	return strength;
}

//---------------------------------------------------------------------------------
//----------------------------------------------------------------------------------

// Mutators 
//----------------------------------------------------------------------------------

void Monster::set_name(string name) 
{
	this->name = name;
}

void Monster::set_health(int health) 
{
	this->health = health;
}

void Monster::set_armor(int armor) 
{
	this->armor = armor;
}

void Monster::set_strength(int strength) 
{
	this->strength = strength;
}

